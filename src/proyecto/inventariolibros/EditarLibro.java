    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton2ActionPerformed
        try {
            String ISBN = jTextField1.getText();
            int edicion = Integer.parseInt(jTextField6.getText());
            int fechaPublicacion = Integer.parseInt(jTextField2.getText());
            String titulo = jTextField7.getText();
            String nombreAutor = jTextField3.getText();
            String primerApellidoAutor = jTextField8.getText();
            String segundoApellidoAutor = jTextField4.getText();
            int numPaginas = Integer.parseInt(jTextField11.getText());
            String categoria = jTextField5.getText();
            String editorial = jTextField12.getText();

            LibroDAO consulta1 = new LibroDAO();
            Libro libro = new Libro(ISBN, edicion, fechaPublicacion, titulo, nombreAutor, primerApellidoAutor,
                    segundoApellidoAutor, numPaginas, categoria, editorial);

            int registrosActualizados = consulta1.actualizarRegistro(libro);
            System.out.println(registrosActualizados);
            if (registrosActualizados == 1) {
                jLabel12.setText("Los cambios en el libro se realizaron correctamente.");
            } else {
                jLabel12.setText("Error, revisa los datos e intenta de nuevo.");
            }
        } catch (Exception e) {
            jLabel12.setText("Ocurrio un error al modificar el libro, intenta de nuevo.");
        }
    }// GEN-LAST:event_jButton2ActionPerformed

